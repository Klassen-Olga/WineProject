<h3>Programmfunktionalität</h3>
<br>
<h4>Accountverwaltung</h4>
<p>Der Benutzer kann sich Registrieren und seine persönlichen Daten (Name, Adresse, Passwort…) ändern.</p>
<p>Der Benutzer erhält Einblick über seine Bestellhistorie und er erhält Informationen wie z.B. Preis oder Produktkategorie für alle Produkte einer Bestellung.</p>
<p>Der Benutzer kann entscheiden ober nach dem Schließen des Browsers eingeloggt bleiben möchte.</p>
<p>Das System ist darauf ausgelegt in der Bestellübersicht für jedes Produkt genau ein bild anzuzeigen</p>
<br>
<h4>Kasse</h4>
<p>Die Lieferadresse kann vom Benutzer angepasst werden.</p>
<p>Der Benutzer muss eine Zahlungsmethode Wählen.</p>
<p>Vor dem Absenden der Bestellung erhält der Benutzer Informationen über Empfänger, Kosten und Lieferadresse.</p>
<br>
<h4>Produktauswahl</h4>
<p>Das System bietet die Möglichkeit Produkte anhand Ihrer Kategorien (z.B. Red wines, Accessories…) zu filtern.</p>
<p>Weiterhin kann nach Region Jahr und Preis selektiert werden.</p>
<p>Produkte können in den Warenkorb gelegt werden.</p>
<p>Für alle Produkt gibt es eine Einzel Ansicht mit detaillierten Informationen wie z.B. Preis, bottle size und color.</p>
<p>Produkte werden seitenweise angezeigt</p>
<p>Das System ist darauf ausgelegt für jedes Produkt genau ein bild anzuzeigen</p>
<br>
<h4>Warenkorb</h4>
<p>Der Benutzer erhält Informationen über Stückzahl, Einzelpreise und Gesamtpreis für alle Produkte im Warenkorb.</p>
<p>Die Anzahl der Produkte im Warenkorb kann geändert werden.</p>
<p>Produkte können aus dem Warenkorb entfernt werden.</p>
<p>Vom Warenkorb kann zur Einzelansicht eines Produktes oder zur Kasse navigiert werden.</p>
<p>Das System ist darauf ausgelegt für jedes Produkt genau ein bild anzuzeigen</p>
<br>
<h4>Weininformation</h4>
<p>Der Benutzer enthält die Informationen über Geschichte, Anbaugebiete und Traubensorten. Er kann entscheiden ob Texte ein oder ausgeblendet werden.</p>
<br>
<h3>Abgrenzungskriterien</h3>
<p>Es gibt keine Administratorsicht. Die Produkte müssen per Hand von Administrator eingefügt, geändert, gelöscht werden. Der Benutzer kann sein Account nicht löschen.</p>
<p>Keine Zahlungsabwicklung und keine Lieferung steht zur Verfügung.</p>
<br>
<h3>Geschäftsregeln</h3>
<br>
<p>Das Zahlungsdatum muss von S&K wine depote per Hand in das Datenbanksystem eingetragen werden.</p>
<p>Das System setzt voraus, dass die Ware von S&K immer nach einem Tag verschickt wird.</p>
<p>Ansonsten muss das Datum in der Datenbank per Hand geändert werden.</p>
<p>Die Produktpreise können inclusive Rabatte angezeigt, zum Warenkorb eingefügt und bestellt werden (zum order/orderitem gespeichert).</p>
<p>Die Produktrabatte werden in der Datenbank festgelegt.</p>
<p>Falls es kein Rabatt gibt wird der Standardpreis vom Produkt angezeigt.</p>
<p>Die Benutzerangaben können über sein Geschlecht und sein Geburtsdatum nicht geändert werden.</p>
<p>Bezahlung ist nur mittels Paypal und Nachnahme möglich.</p>
<p>Die Lieferadressen sind nur für Länder im Europäischen Raum gültig.</p>
<p>Bezahlung und Preisangaben sind in Euro umzusetzen.</p>
<p>Die Sprache für die Seite ist Englisch</p>
<p>Es können maximal 10 Produkte auf einmal in den Warenkorb gelegt werden</p>
<p>Versandkosten entfallen bei der Bestellung ab 50 Euro, ansonsten kostet die Lieferung 3.49 Euro</p>
<p>Zulieferer können in die Datenbank eingetragen werden (für spätere erweiterungen), die Daten werden auf der Webseite nicht verwendet.</p>
