
<h1>Ausgangssintuation</h1>
<div class="imprint">
<p>Unser Kunde ist ein deutscher Weinhändler (Einzelhändler) und er will seinen Vertrieb auf den Onlinehandel ausweiten. Dafür benötigt er eine entsprechende Plattform, mit der er sein Unternehmen mit den entsprechenden Produkten präsentieren kann. Das Sortiment enthält Wein und Sekt der beliebtesten Weinhersteller weltweit. Darüber hinaus wird Zubehör wie Weinkarton, Korkenzieher, Champagner Ausgießer etc. angeboten .
  Die Webpräsenz umfasst die Accountverwaltung der Benutzer, die Verkaufsabwicklung
  (Warenkorb, Zahlungsmethode und Lieferdaten), Produktbeschreibung in verschiedenen Kategorien mit visueller Darstellung, Datenschutzerklärung / Impressum, Bildergalerie und einen Informationsbereich mit allgemeinen Informationen für Weinliebhaber.
  Die Webseite wird ausschließlich von privat Personen aus Europa genutzt, die Bezahlung ist lediglich in Euro möglich.
</p>
